package api.vo;

public class APIRibbonApiVO {

	/** 차종코드 */
	private String code;

	/** 차종명 */
	private String name;

	/** 연료타입 */
	private String fuel;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFuel() {
		return fuel;
	}

	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
}
