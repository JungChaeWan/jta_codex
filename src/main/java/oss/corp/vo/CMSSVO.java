package oss.corp.vo;

public class CMSSVO {

	/** 수수료 번호 */
	private String cmssNum;
	/** 수수료 명칭 */
	private String cmssNm;
	/** 정산 적용 비율 */
	private String adjAplPct;
	/** 등록 일시 */
	private String regDttm;
	/** 등록 아이디 */
	private String regId;
	/** 수정 일시 */
	private String modDttm;
	/** 수정 아이디 */
	private String modId;
	/** 업체 아이디 */
	private String corpId;
	/** 승인 구분 */
	private String confDiv;
	/** 수수료 구분 */
	private String cmssDiv;
	
	
	public String getCmssNum() {
		return cmssNum;
	}
	public void setCmssNum(String cmssNum) {
		this.cmssNum = cmssNum;
	}
	public String getCmssNm() {
		return cmssNm;
	}
	public void setCmssNm(String cmssNm) {
		this.cmssNm = cmssNm;
	}
	public String getAdjAplPct() {
		return adjAplPct;
	}
	public void setAdjAplPct(String adjAplPct) {
		this.adjAplPct = adjAplPct;
	}
	public String getRegDttm() {
		return regDttm;
	}
	public void setRegDttm(String regDttm) {
		this.regDttm = regDttm;
	}
	public String getRegId() {
		return regId;
	}
	public void setRegId(String regId) {
		this.regId = regId;
	}
	public String getModDttm() {
		return modDttm;
	}
	public void setModDttm(String modDttm) {
		this.modDttm = modDttm;
	}
	public String getModId() {
		return modId;
	}
	public void setModId(String modId) {
		this.modId = modId;
	}
	public String getCorpId() {
		return corpId;
	}
	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
	public String getConfDiv() {
		return confDiv;
	}
	public void setConfDiv(String confDiv) {
		this.confDiv = confDiv;
	}
	public String getCmssDiv() {
		return cmssDiv;
	}
	public void setCmssDiv(String cmssDiv) {
		this.cmssDiv = cmssDiv;
	}


}
