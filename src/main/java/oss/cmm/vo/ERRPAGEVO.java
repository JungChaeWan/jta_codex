package oss.cmm.vo;

public class ERRPAGEVO {

	private String errCord;
	private String errMsg;
	private String rtnUrl;
	private String buttonText;
	
	
	public String getErrCord() {
		return errCord;
	}
	public void setErrCord(String errCord) {
		this.errCord = errCord;
	}
	public String getErrMsg() {
		return errMsg;
	}
	public void setErrMsg(String errMsg) {
		this.errMsg = errMsg;
	}
	public String getRtnUrl() {
		return rtnUrl;
	}
	public void setRtnUrl(String rtnUrl) {
		this.rtnUrl = rtnUrl;
	}
	public String getButtonText() {
		return buttonText;
	}
	public void setButtonText(String buttonText) {
		this.buttonText = buttonText;
	}

	

}
