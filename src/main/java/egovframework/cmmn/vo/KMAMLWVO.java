package egovframework.cmmn.vo;

public class KMAMLWVO {

	/** 발표일자 */
	private String baseDt;
	/** 시간대 구분 */
	private String timeDiv;
	/** 날씨예보 */
	private String wfNm;
	/** 최저기온 */
	private String taMin;
	/** 최고기온 */
	private String taMax;
	/** 요일 */
	private String yoil;
	
	public String getBaseDt() {
		return baseDt;
	}
	public void setBaseDt(String baseDt) {
		this.baseDt = baseDt;
	}
	public String getTimeDiv() {
		return timeDiv;
	}
	public void setTimeDiv(String timeDiv) {
		this.timeDiv = timeDiv;
	}
	public String getWfNm() {
		return wfNm;
	}
	public void setWfNm(String wfNm) {
		this.wfNm = wfNm;
	}
	public String getTaMin() {
		return taMin;
	}
	public void setTaMin(String taMin) {
		this.taMin = taMin;
	}
	public String getTaMax() {
		return taMax;
	}
	public void setTaMax(String taMax) {
		this.taMax = taMax;
	}
	public String getYoil() {
		return yoil;
	}
	public void setYoil(String yoil) {
		this.yoil = yoil;
	}


}
